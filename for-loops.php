<?php

// FOR LOOP WHICH RETURNS ALL NUMBERS BETWEEN 0 AND 9 
    for ($i = 0; $i < 10; $i++) {
        echo $i."<br>";
    }

echo "<br><br>";

// FOR LOOP WHICH RETURNS ALL EVEN NUMBERS BETWEEN AND INCL. 2 AND 30
    for ($i = 2; $i <= 30; $i = $i + 2) {
        echo $i."<br>";
    }

echo "<br><br>";

// FOR LOOP WHICH RETURNS A REVERSE-COUNT FROM 10 TO 0
    for ($i = 10; $i >= 0; $i--) {
        echo $i."<br>";
    }

?>